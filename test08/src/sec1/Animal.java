package sec1;

public class Animal {
	private int leg;
	private boolean spine;
	
	public Animal(int leg, boolean spine) {
		this.leg = leg;
		this.spine = spine;
	}

	public int getLeg() {
		return leg;
	}

	public void setLeg(int leg) {
		this.leg = leg;
	}

	public boolean isSpine() {
		return spine;
	}

	public void setSpine(boolean spine) {
		this.spine = spine;
	}
	
	
}
